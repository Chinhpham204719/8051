	
;define
BUTTON_1 BIT P3.3
BUTTON_2 BIT P3.4
BUTTON_3 BIT P3.5
BUTTON_4 BIT P3.2

ORG 00H
SETB BUTTON_1
SETB BUTTON_2
SETB BUTTON_3
SETB BUTTON_4

SETB P2.3
CPL P2.3

MAIN:
	JNB BUTTON_1,NUM_1 ; IF BUTTON_1 = 0, JUMP TO NUM_1
	JNB BUTTON_2,NUM_2 ; IF BUTTON_2 = 0, JUMP TO NUM_2
	JNB BUTTON_3,NUM_3 ; IF BUTTON_3 = 0, JUMP TO NUM_3
	JNB BUTTON_4,NUM_4 ; IF BUTTON_4 = 0, JUMP TO NUM_4	
	
NUM_0:
	MOV P1,#0C0H
	JMP MAIN
NUM_1:
	MOV P1,#0F9H
	JMP MAIN
NUM_2:
	MOV P1,#0A4H
	JMP MAIN
NUM_3:
	MOV P1,#0B0H
	JMP MAIN
NUM_4:
	MOV P1,#99H
	JMP MAIN


	END